      ARG_TIME: ts,
      ARG_SPEED: speedMs,
      ARG_SPEED_ACCURACY: 1.0,
      ARG_MOCKED: true,
    });
  }

  /// Calculates the azimuth in degrees given two {@link Coordinate} composing a line.
  ///
  /// Note that the coords order is important and will differ of 180.
  ///
  /// @param c1 first coordinate (used as origin).
  /// @param c2 second coordinate.
  /// @return the azimuth angle.
  static double _azimuth(Coordinate c1, Coordinate c2) {
    // vertical
    if (c1.x == c2.x) {
      if (c1.y == c2.y) {
        // same point
        return double.nan;
      } else if (c1.y < c2.y) {
        return 0.0;
      } else if (c1.y > c2.y) {
        return 180.0;
      }
    }
    // horiz
    if (c1.y == c2.y) {
      if (c1.x < c2.x) {
        return 90.0;
      } else if (c1.x > c2.x) {
        return 270.0;
      }
    }
    // -> /
    if (c1.x < c2.x && c1.y < c2.y) {
      double tanA = (c2.x - c1.x) / (c2.y - c1.y);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD);
    }
    // -> \
    if (c1.x < c2.x && c1.y > c2.y) {
      double tanA = (c1.y - c2.y) / (c2.x - c1.x);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD) + 90.0;
    }
    // <- /
    if (c1.x > c2.x && c1.y > c2.y) {
      double tanA = (c1.x - c2.x) / (c1.y - c2.y);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD) + 180;
    }
    // <- \
    if (c1.x > c2.x && c1.y < c2.y) {
      double tanA = (c2.y - c1.y) / (c1.x - c2.x);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD) + 270;
    }

    return double.nan;
  }
}

